package futebol;

public class treinador {
    private String nome;
    private int idade;
    private int salario;
    private String estiloJogo;
    private String treinamentoFisico;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getSalario() {
        return salario;
    }

    public void setSalario(int salario) {
        this.salario = salario;
    }

    public String getEstiloJogo() {
        return estiloJogo;
    }

    public void setEstiloJogo(String estiloJogo) {
        this.estiloJogo = estiloJogo;
    }

    public String getTreinamentoFisico() {
        return treinamentoFisico;
    }

    public void setTreinamentoFisico(String treinamentoFisico) {
        this.treinamentoFisico = treinamentoFisico;
    }
}
